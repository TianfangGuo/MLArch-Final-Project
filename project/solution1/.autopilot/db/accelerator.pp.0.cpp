void gustavson_hls(
    int M, int N, int nnzA,
    const float valuesA[256],
    const int col_indicesA[256],
    const int row_ptrA[16 + 1],
    const float B[16 * 16],
    float C[16 * 16]
) {
#pragma HLS INTERFACE ap_memory port=valuesA depth=256
#pragma HLS INTERFACE ap_memory port=col_indicesA depth=256
#pragma HLS INTERFACE ap_memory port=row_ptrA depth=16 +1
#pragma HLS INTERFACE ap_memory port=B depth=16*16
#pragma HLS INTERFACE ap_memory port=C depth=16*16

#pragma HLS INTERFACE s_axilite port=M bundle=control
#pragma HLS INTERFACE s_axilite port=N bundle=control
#pragma HLS INTERFACE s_axilite port=nnzA bundle=control
#pragma HLS INTERFACE s_axilite port=return bundle=control


#pragma HLS PIPELINE II=1


 init_output:
    for (int i = 0; i < 16 * 16; i++) {
#pragma HLS PIPELINE II=1
 C[i] = 0;
    }

    compute_rows:
    for (int i = 0; i < M; i++) {
        float row_acc[16] = {0};
#pragma HLS ARRAY_PARTITION variable=row_acc complete dim=1

 compute_nnz:
        for (int idx = row_ptrA[i]; idx < row_ptrA[i + 1]; idx++) {
#pragma HLS PIPELINE II=1
 int col = col_indicesA[idx];
            float val = valuesA[idx];

            compute_inner:
            for (int j = 0; j < N; j++) {
#pragma HLS UNROLL factor=4
 row_acc[j] += val * B[col * N + j];
            }
        }

        write_back:
        for (int j = 0; j < N; j++) {
#pragma HLS PIPELINE II=1
 C[i * N + j] = row_acc[j];
        }
    }
}
